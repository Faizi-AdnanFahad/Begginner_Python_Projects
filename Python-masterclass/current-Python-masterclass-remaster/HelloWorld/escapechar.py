splitString = "this staring has been \nsplit over\nseveral\nlines"
print(splitString)

tabbedString = "1\t2\t3\t4\t5"
print(tabbedString)

print('The pet show owner said "No, no, \'e\'s uh,...he\'s resting"')

# backslash help us to skip a character.
print("The pet show owner said \"No, no, 'e 's uh,...he's resting\"")

# No need for to skip when triple quote is used
print("""The pet show owner said "No, no, 'e's uh,...he's resting" """)
print()

# this will be printed into several lines because of the triple quote
print("""This string has been 
split over 
several 
lines""")

print()

# these 3 backslash will avoid splitting the lines and will skip the last character at each line which is a hidden *n*
print("""This string has been \
split over \
several \
lines""")

